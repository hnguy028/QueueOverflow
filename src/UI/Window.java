package UI;

/**
 * @author hinguyen
 * 
 * Top level container of all objects and ui components (ie. board and canvas)
 */
public class Window {
	
	private int height;
	private int width;
	
	/**
	 * 
	 */
	public Window(int _height, int _width) {
		this.height = height;
		this.width = width;
	}
	
	public void resize() {
		
	}

	public int getHeight() {
		return height;
	}

	public int getWidth() {
		return width;
	}
}